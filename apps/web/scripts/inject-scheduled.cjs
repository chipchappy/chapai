#!/usr/bin/env node
// Post-build: write a thin worker wrapper that adds a `scheduled` handler on top
// of the OpenNext-generated worker.js. Cloudflare cron triggers route here and
// dispatch to the appropriate /api/cron/* endpoint based on event.cron.
const fs = require("node:fs");
const path = require("node:path");

const OUT_DIR = path.join(__dirname, "..", ".open-next");
const ORIGINAL = path.join(OUT_DIR, "worker.js");
const WRAPPER = path.join(OUT_DIR, "worker-with-cron.js");

if (!fs.existsSync(ORIGINAL)) {
  console.error(`[inject-scheduled] missing ${ORIGINAL}; aborting`);
  process.exit(1);
}

const wrapper = `// Auto-generated by scripts/inject-scheduled.cjs — DO NOT EDIT.
// Wraps the OpenNext worker with a Cloudflare \`scheduled\` handler that
// dispatches each cron expression to its dedicated /api/cron/* endpoint.
import opennextWorker from "./worker.js";

// Map cron expression → endpoint path. Keep in sync with wrangler.jsonc \`triggers.crons\`.
const CRON_ROUTES = {
  "0 13 * * *": "/api/cron/streak-protection",   // 8am ET daily
  "0 12 * * *": "/api/cron/qotd-send",            // 7am ET daily
  "*/15 * * * *": "/api/cron/generate-drafts",    // every 15 min — NGN draft accrual (self-throttles at backlog cap)
};

function buildCronRequest(env, pathname) {
  const origin =
    env.NEXT_PUBLIC_APP_URL ||
    env.NEXTAUTH_URL ||
    "https://claritynclex.com";
  const headers = new Headers({
    "cf-cron": "1",
    "user-agent": "cloudflare-cron-trigger",
  });
  if (env.CRON_SECRET) {
    headers.set("authorization", \`Bearer \${env.CRON_SECRET}\`);
  }
  return new Request(new URL(pathname, origin), {
    method: "POST",
    headers,
  });
}

export default {
  ...opennextWorker,
  async scheduled(event, env, ctx) {
    try {
      const route = CRON_ROUTES[event?.cron];
      if (!route) {
        console.warn("[scheduled] unmapped cron expression", event?.cron);
        return;
      }
      const req = buildCronRequest(env, route);
      const handle = opennextWorker.fetch(req, env, ctx);
      if (ctx && typeof ctx.waitUntil === "function") {
        ctx.waitUntil(handle);
      } else {
        await handle;
      }
    } catch (err) {
      console.error("[scheduled] cron dispatch failed", err);
    }
  },
};
`;

fs.writeFileSync(WRAPPER, wrapper, "utf8");
console.log(`[inject-scheduled] wrote ${path.relative(process.cwd(), WRAPPER)}`);
