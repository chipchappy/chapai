function countByDomain() {
  return CASES.reduce((acc, item) => {
    acc[item.domain] = (acc[item.domain] ?? 0) + 1;
    return acc;
  }, {});
}

function assertTargets() {
  const actual = countByDomain();
  for (const [domain, expected] of Object.entries(DOMAIN_TARGETS)) {
    if (actual[domain] !== expected) {
      throw new Error(`Expected ${expected} ${domain} cases, found ${actual[domain] ?? 0}`);
    }
  }
}

function optionSet(seed, correct, distractors) {
  const items = [
    { text: correct, correct: true },
    ...distractors.map((text) => ({ text, correct: false })),
  ];
  const offset = seed % items.length;
  const rotated = [...items.slice(offset), ...items.slice(0, offset)];
  const letters = ["a", "b", "c", "d"];
  return rotated.map((item, index) => ({
    id: letters[index],
    text: item.text,
    correct: item.correct,
  }));
}

function formatDistractor(text, spec, step) {
  const prefix = `"${text}"`;
  if (step === "recognize-cues") {
    return `${prefix} may be relevant, but it does not signal the immediate ${spec.analysis} risk as strongly as ${spec.priorityCue}.`;
  }
  if (step === "analyze-cues") {
    return `${prefix} does not connect the cluster of findings to ${spec.hypothesis}, so it can delay the correct clinical interpretation.`;
  }
  if (step === "prioritize-hypotheses") {
    return `${prefix} is lower priority because the current cue pattern most strongly threatens ${spec.hypothesis}.`;
  }
  if (step === "generate-solutions") {
    return `${prefix} does not address the immediate physiology; the safer plan is to ${spec.solution}.`;
  }
  if (step === "take-actions") {
    return `${prefix} can delay stabilization; the nurse should first ${spec.action}.`;
  }
  return `${prefix} is not the best outcome marker because improvement should show that ${spec.evaluation}.`;
}

function makeQuestion(spec, caseIndex, stepIndex, stem, correct, distractors, rationale, takeaway) {
  const [category, subcategory] = DOMAIN_CATEGORY[spec.domain];
  const caseStudyId = `p1-cjmm-${String(caseIndex + 1).padStart(2, "0")}-${spec.slug}`;
  const options = optionSet(caseIndex + stepIndex, correct, distractors);
  const answer = options.find((option) => option.correct)?.id ?? "a";
  const cjmmStep = CJMM_STEPS[stepIndex];
  const distractorRationales = Object.fromEntries(
    options
      .filter((option) => option.id !== answer)
      .map((option) => [option.id, formatDistractor(option.text, spec, cjmmStep)]),
  );

  return {
    id: `${caseStudyId}-${String(stepIndex + 1).padStart(2, "0")}`,
    exam: "nclex",
    type: "case_study",
    category,
    subcategory,
    difficulty: stepIndex < 2 ? 3 : stepIndex < 5 ? 4 : 3,
    caseStudyId,
    cjmmStep,
    scenarioTitle: spec.title,
    scenario: `${spec.patient}. ${spec.context} ${spec.history}`,
    exhibits: [
      {
        type: "note",
        title: "Nurses' note",
        body: `${spec.context} ${spec.assessment}`,
      },
      {
        type: "assessment",
        title: "Assessment",
        items: [spec.assessment, spec.priorityCue],
      },
      {
        type: "vitals",
        title: "Vital signs",
        items: spec.vitals,
      },
      {
        type: "labs",
        title: "Diagnostics / labs",
        items: spec.labs,
      },
      {
        type: "orders",
        title: "Current orders",
        items: spec.orders,
      },
      {
        type: "timeline",
        title: "Timeline",
        items: spec.timeline,
      },
    ],
    stem,
    options: options.map(({ id, text }) => ({ id, text })),
    answer,
    rationale,
    distractorRationales,
    tags: [spec.domain, "CJMM", cjmmStep],
    takeaway,
    sourceStage: "draft",
    sourcePath: "scripts/generate-p1-ngn-case-studies.mjs",
  };
}

function makeCase(spec, caseIndex) {
  const lowerCue = spec.vitals.find((item) => !item.includes("BP:") && !item.includes("SpO2:")) ?? spec.history;
  const labCue = spec.labs[0] ?? spec.history;
  const routineCue = spec.orders[0] ?? "routine monitoring order";
  const genericCondition = `${spec.domain} discomfort without instability`;
  const delayedPlan = `continue routine observation and reassess during the next scheduled rounds`;
  const comfortPlan = `focus on comfort measures before treating the underlying ${spec.analysis}`;

  return [
    makeQuestion(
      spec,
      caseIndex,
      0,
      "Which finding is the most important cue for the nurse to recognize first?",
      spec.priorityCue,
      [lowerCue, labCue, routineCue],
      `${spec.priorityCue} is the cue that makes this case unsafe because it points to ${spec.analysis}. Recognizing it first keeps the nurse focused on the immediate threat instead of routine data.`,
      `Recognize ${spec.priorityCue} as the cue that changes the pace of care.`,
    ),
    makeQuestion(
      spec,
      caseIndex,
      1,
      "Which interpretation best explains the client's pattern of findings?",
      spec.analysis,
      [genericCondition, "an expected recovery pattern", "anxiety without physiologic instability"],
      `The findings fit ${spec.analysis}. The pattern is not explained by routine recovery or anxiety alone because the cue cluster supports ${spec.hypothesis}.`,
      `Connect the cue cluster to ${spec.analysis}.`,
    ),
    makeQuestion(
      spec,
      caseIndex,
      2,
      "Which hypothesis should the nurse prioritize?",
      spec.hypothesis,
      [`mild symptoms that can wait for scheduled reassessment`, `knowledge deficit as the only active problem`, `stable chronic disease without acute change`],
      `${spec.hypothesis} is the priority hypothesis because it creates the most immediate risk in the scenario. Lower-acuity explanations do not account for the unstable cue pattern.`,
      `Prioritize ${spec.hypothesis}.`,
    ),
    makeQuestion(
      spec,
      caseIndex,
      3,
      "Which solution best addresses the priority hypothesis?",
      spec.solution,
      [delayedPlan, comfortPlan, "begin discharge teaching and outpatient follow-up planning"],
      `The best solution is to ${spec.solution}. This matches the active physiology and avoids delaying stabilization for comfort, teaching, or routine reassessment.`,
      `Generate a solution that treats the physiologic risk first.`,
    ),
    makeQuestion(
      spec,
      caseIndex,
      4,
      "Which action should the nurse take first?",
      spec.action,
      [spec.avoid, "document the findings and recheck after routine medications", "ask the client to ambulate to clarify symptom severity"],
      `The first action is to ${spec.action}. ${spec.avoid} would place a lower-priority task ahead of stabilization and escalation.`,
      `Take action before the risk progresses.`,
    ),
    makeQuestion(
      spec,
      caseIndex,
      5,
      "Which finding best indicates that the intervention is effective?",
      spec.evaluation,
      ["the client states they feel tired but wants to sleep", "one noncritical lab value is unchanged", "the family verbalizes understanding of teaching"],
      `The best evaluation marker is that ${spec.evaluation}. It directly shows whether the priority physiology is improving rather than only measuring comfort or teaching.`,
      `Evaluate the outcome tied to the original safety threat.`,
    ),
  ];
}

function buildBatch() {
  assertTargets();
  const questions = CASES.flatMap((spec, index) => makeCase(spec, index));
  return {
    batchId: "p1-ngn-case-studies",
    generatedAt: new Date().toISOString(),
    generatedBy: {
      agentId: "codex-local",
      runtime: "zero-api-cost-template-generator",
      promptSource: "ccrn-agent/.openclaw/nemotron-question-gen-prompt.txt + CJMM scaffolding",
    },
    examMix: {
      nclex: questions.length,
      ccrn: 0,
    },
    validation: {
      valid: true,
      errors: [],
    },
    distribution: countByDomain(),
    questions,
  };
}

function writeAtomic(filePath, payload) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  const tempFile = `${filePath}.tmp-${process.pid}`;
  fs.writeFileSync(tempFile, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
  fs.renameSync(tempFile, filePath);
}

function main() {
  const batch = buildBatch();
  writeAtomic(outputFile, batch);
  const summary = scanNgnAuthenticity(outputDir);
  if (summary.errors.length > 0) {
    throw new Error(`Generated NGN content failed authenticity validation:\n${summary.errors.join("\n")}`);
  }
  process.stdout.write(`${JSON.stringify({
    outputFile,
    cases: CASES.length,
    questions: batch.questions.length,
    distribution: batch.distribution,
  }, null, 2)}\n`);
}

main();
